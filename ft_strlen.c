#include "libft_ult.h"

ssize_t	ft_strlen(const char *s)
{
	ssize_t	i;

	i = 0;
	if (s == 0)
		return (-1);
	if (s[0] == 0)
		return (0);
	while (s[i])
		i++;
	return (i);
}
